import Foundation

enum BinaryOperation: String, EnumCollection {
    case pow = "x^y"
    case mod = "mod"
    case plus = "+"
    case multiply = "×"
    case division = "÷"
    case minus = "−"
    case EEOperation = "EE"
    case powReverse = "x^(1/y)"
    case yPowX = "y^x"
    case logY = "log y"
}
