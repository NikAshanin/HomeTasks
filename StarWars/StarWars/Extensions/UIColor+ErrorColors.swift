import UIKit
extension UIColor {
    static func errorColor() -> UIColor {
        return #colorLiteral(red: 1, green: 0.5833955407, blue: 0.5876240134, alpha: 1)
    }
    static func okColor() -> UIColor {
        return #colorLiteral(red: 0.722771585, green: 0.8874585032, blue: 0.5934467316, alpha: 1)
    }
}
